CREATE TABLE IF NOT EXISTS user_global_search_preference (
                                                             id         SERIAL PRIMARY KEY,
                                                             preference TEXT,
                                                             country_of_residence_id INTEGER REFERENCES countries(id) ON DELETE SET NULL,
    language_id             INTEGER REFERENCES languages(id) ON DELETE SET NULL,
    user_id    INTEGER NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE
    );

CREATE TABLE IF NOT EXISTS user_ingredient (
                                               id              SERIAL PRIMARY KEY,
                                               name            TEXT NOT NULL,
                                               fill_percentage FLOAT NOT NULL, -- В Postgres краще FLOAT або NUMERIC
                                               photo_link      TEXT,
                                               user_id         INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE
    );

CREATE TABLE IF NOT EXISTS user_kitchen_appliances (
                                                       id          SERIAL PRIMARY KEY,
                                                       name        TEXT NOT NULL,
                                                       description TEXT,
                                                       user_id     INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE
    );

CREATE TABLE IF NOT EXISTS refresh_tokens (
                                              id         SERIAL PRIMARY KEY,
                                              jti        TEXT NOT NULL UNIQUE,
                                              user_id    INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    expires_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );

CREATE INDEX IF NOT EXISTS idx_refresh_tokens_jti ON refresh_tokens(jti);
CREATE INDEX IF NOT EXISTS idx_refresh_tokens_user_id ON refresh_tokens(user_id);