CREATE TABLE IF NOT EXISTS users (
                                     id         SERIAL PRIMARY KEY,
                                     name       TEXT NOT NULL UNIQUE,
                                     password   TEXT NOT NULL
    );
CREATE TABLE password_init_tokens (
                                      id          SERIAL PRIMARY KEY,
                                      user_id     INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                                      token       VARCHAR(128) NOT NULL UNIQUE,  -- криптографічно стійкий випадковий токен
                                      expires_at  TIMESTAMPTZ NOT NULL,
                                      used_at     TIMESTAMPTZ NULL,              -- NULL = не використаний, NOT NULL = вже активований
                                      created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE TABLE IF NOT EXISTS user_cookware (
                                             id          SERIAL PRIMARY KEY, -- Рекомендую додавати ID у кожну таблицю
                                             name        TEXT NOT NULL,
                                             description TEXT,
                                             user_id     INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE
    );
CREATE TABLE IF NOT EXISTS languages (
